#!/usr/bin/env python3
"""Convert quantity takeoff data to a formatted Excel workbook with summary and detail tabs."""

import argparse
import json
import sys
from datetime import datetime
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from shared import safe_output_path

def takeoff_to_xlsx(data_path, output="takeoff.xlsx", project="", sheets=""):
    try:
        from openpyxl import Workbook
        from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    except ImportError:
        print("ERROR: openpyxl not installed. Run: pip install openpyxl")
        sys.exit(1)

    with open(data_path) as f:
        items = json.load(f)

    wb = Workbook()
    thin = Border(left=Side("thin"), right=Side("thin"), top=Side("thin"), bottom=Side("thin"))
    hdr_fill = PatternFill(start_color="2E75B6", end_color="2E75B6", fill_type="solid")
    hdr_font = Font(color="FFFFFF", bold=True)

    # --- Summary Tab ---
    ws = wb.active
    ws.title = "Summary"
    ws["A1"] = f"Quantity Takeoff — {project}"
    ws["A1"].font = Font(bold=True, size=14)
    ws["A2"] = f"Sheets: {sheets} | Date: {datetime.now().strftime('%Y-%m-%d')}"
    ws["A2"].font = Font(italic=True, color="666666")

    # Group by category
    categories = {}
    for item in items:
        cat = item.get("category", "Uncategorized")
        if cat not in categories:
            categories[cat] = {"items": [], "total_qty": 0}
        categories[cat]["items"].append(item)
        categories[cat]["total_qty"] += item.get("quantity", 1)

    row = 4
    for hdr_text in ["Category", "Item Count", "Total Quantity", "Unit"]:
        c = ["Category", "Item Count", "Total Quantity", "Unit"].index(hdr_text) + 1
        cell = ws.cell(row=row, column=c, value=hdr_text)
        cell.fill = hdr_fill
        cell.font = hdr_font
        cell.border = thin

    for cat, data in sorted(categories.items()):
        row += 1
        unit = data["items"][0].get("unit", "EA") if data["items"] else "EA"
        for c, val in enumerate([cat, len(data["items"]), data["total_qty"], unit], 1):
            cell = ws.cell(row=row, column=c, value=val)
            cell.border = thin

    # --- Detail Tab ---
    detail = wb.create_sheet("Detail")
    headers = ["Item #", "Category", "Description", "Sheet", "Location", "Quantity", "Unit", "Notes"]
    for c, h in enumerate(headers, 1):
        cell = detail.cell(row=1, column=c, value=h)
        cell.fill = hdr_fill
        cell.font = hdr_font
        cell.border = thin

    for r, item in enumerate(items, 2):
        vals = [
            r - 1,
            item.get("category", ""),
            item.get("description", ""),
            item.get("sheet", ""),
            item.get("location", ""),
            item.get("quantity", 1),
            item.get("unit", "EA"),
            item.get("notes", ""),
        ]
        for c, v in enumerate(vals, 1):
            cell = detail.cell(row=r, column=c, value=v)
            cell.border = thin

    # --- Notes Tab ---
    notes = wb.create_sheet("Notes")
    notes["A1"] = "Quantity Takeoff Notes"
    notes["A1"].font = Font(bold=True, size=12)
    notes["A3"] = "This takeoff was generated by AI vision analysis and should be verified by a human estimator."
    notes["A4"] = f"Source sheets: {sheets}"
    notes["A5"] = f"Generated: {datetime.now().isoformat()}"
    notes["A6"] = "Tool: construction-skills/quantity-takeoff"

    out = safe_output_path(output)
    wb.save(str(out))
    print(f"OK: {out} ({len(items)} items, {len(categories)} categories)")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Convert takeoff data to Excel")
    parser.add_argument("--data", required=True, help="JSON data file")
    parser.add_argument("--output", "-o", default="takeoff.xlsx")
    parser.add_argument("--project", default="")
    parser.add_argument("--sheets", default="")
    args = parser.parse_args()
    takeoff_to_xlsx(args.data, args.output, args.project, args.sheets)
